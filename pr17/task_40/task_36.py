# Завдання 36 — Порівняння двох множин покупок

list_a = {"молоко", "хліб", "яйця", "масло", "сир"}
list_b = {"хліб", "яйця", "апельсини", "сік"}

print("Куплено обома:", list_a & list_b)
print("Тільки в списку A:", list_a - list_b)
print("Тільки в списку B:", list_b - list_a)
print("Всього унікальних товарів:", len(list_a | list_b))
