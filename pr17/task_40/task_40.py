sales = [
    {"товар": "ноутбук",  "категорія": "техніка",    "сума": 32000, "місяць": "Jan"},
    {"товар": "телефон",  "категорія": "техніка",    "сума": 18000, "місяць": "Jan"},
    {"товар": "куртка",   "категорія": "одяг",       "сума": 3500,  "місяць": "Feb"},
    {"товар": "ноутбук",  "категорія": "техніка",    "сума": 29000, "місяць": "Feb"},
    {"товар": "кросівки", "категорія": "одяг",       "сума": 4200,  "місяць": "Jan"},
    {"товар": "планшет",  "категорія": "техніка",    "сума": 12000, "місяць": "Feb"},
    {"товар": "шапка",    "категорія": "одяг",       "сума": 800,   "місяць": "Feb"},
]

# Унікальні категорії та товари
categories = {s["категорія"] for s in sales}
products = {s["товар"] for s in sales}
print("Категорії:", categories)
print("Товарів:", len(products))

# Сума по категоріях
by_cat = {}
for s in sales:
    by_cat[s["категорія"]] = by_cat.get(s["категорія"], 0) + s["сума"]
print("\nВиручка по категоріях:", by_cat)

# Сума по місяцях
by_month = {}
for s in sales:
    by_month[s["місяць"]] = by_month.get(s["місяць"], 0) + s["сума"]
print("Виручка по місяцях:", by_month)

# Найпопулярніший товар за кількістю продажів
prod_count = {}
for s in sales:
    prod_count[s["товар"]] = prod_count.get(s["товар"], 0) + 1
print("Найчастіше продавано:", max(prod_count, key=prod_count.get))
print("Загальна виручка:", sum(s["сума"] for s in sales))
