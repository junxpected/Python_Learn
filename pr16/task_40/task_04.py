# Завдання 4 — Видалення елементів зі списку (remove, pop)

fruits = ["яблуко", "персик", "банан", "вишня", "виноград"]
fruits.remove("банан")          # видалення за значенням
popped = fruits.pop()           # видалення останнього
print("Список після видалення:", fruits)
print("Видалений елемент:", popped)
